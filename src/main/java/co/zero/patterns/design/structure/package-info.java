/**
 * This package contains samples of how to implement structure patterns
 * 
 * <p>
 * Structural patterns are concerned with how classes and objects are composed 
 * to form larger structures. Structural class patterns use inheritance to compose 
 * interfaces or implementations. As a simple example, consider how multiple 
 * inheritance mixes two or more classes into one. The result is a class that 
 * combines the properties of its parent classes. This pattern is particularly 
 * useful for making independently developed class libraries work together. 
 * Rather than composing interfaces or implementations, structural object patterns 
 * describe ways to compose objects to realize new functionality. The added flexibility 
 * of object composition comes from the ability to change the composition at run-time, 
 * which is impossible with static class composition.
 * </p>
 * 
 * Definition taken from http://www.javacodegeeks.com/2015/09/introduction-to-design-patterns.html
 */
