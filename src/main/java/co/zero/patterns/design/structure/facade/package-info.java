/**
 * This package contains an implementation of Facade pattern
 * 
 * <p>The Facade Pattern makes a complex interface easier to use, using a Facade class. 
 * The Facade Pattern provides a unified interface to a set of interface in a subsystem. 
 * Facade defines a higher-level interface that makes the subsystem easier to use.
 * The Facade unifies the complex low-level interfaces of a subsystem in-order to provide 
 * a simple way to access that interface. It just provides a layer to the complex interfaces 
 * of the sub-system which makes it easier to use.The Facade do not encapsulate the subsystem 
 * classes or interfaces; it just provides a simplified interface to their functionality. 
 * A client can access these classes directly. It still exposes the full functionality of 
 * the system for the clients who may need it.</p>
 * 
 * Definition taken from <a href="http://www.javacodegeeks.com/2015/09/facade-design-pattern.html">URL Pattern</a>
 */
