/**
 * This package contains samples of how to implement creational patterns
 * 
 * <p>Creational design patterns are used to design the instantiation process of objects. 
 * The creational pattern uses the inheritance to vary the object creation.There are two 
 * recurring themes in these patterns. First, they all encapsulate knowledge about which 
 * concrete classes the system uses. Second, they hide how instances of these classes 
 * are created and put together. All the system at large knows about the objects is their 
 * interfaces as defined by abstract classes. Consequently, the creational patterns give 
 * you a lot of flexibility in what gets created, who creates it, how it gets created, 
 * and when. There can be some cases when two or more patterns looks fit as a solution 
 * to a problem. At other times, the two patterns complement each other for example; 
 * Builder can be used with other pattern to implements which components to get built.</p>
 * 
 * Definition taken from http://www.javacodegeeks.com/2015/09/introduction-to-design-patterns.html
 */
