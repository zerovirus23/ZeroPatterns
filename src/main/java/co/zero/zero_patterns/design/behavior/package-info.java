/**
 * This package contains samples of how to implement behavioral patterns
 * 
 * <p>Behavioral patterns are concerned with algorithms and the assignment 
 * of responsibilities between objects. Behavioral patterns describe not just 
 * patterns of objects or classes but also the patterns of communication between them. 
 * These patterns characterize complex control flow that’s difficult to follow at run-time. 
 * They shift your focus away from flow of control to let you concentrate just on 
 * the way objects are interconnected.Behavioral object patterns use object composition 
 * rather than inheritance. Some describe how a group of peer objects cooperate to perform 
 * a task that no single object can carry out by itself. An important issue here is how 
 * peer objects know about each other. Peers could maintain explicit references to each 
 * other, but that would increase their coupling. In the extreme, every object would know 
 * about every other. The Mediator pattern avoids this by introducing a mediator object 
 * between peers. The mediator provides the indirection needed for loose coupling.
 * </p>
 * 
 * Definition taken from http://www.javacodegeeks.com/2015/09/introduction-to-design-patterns.html
 */
